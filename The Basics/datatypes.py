#list data type
fruits = ["apple", "banana", "cherry"]
languages  = ["python", "java", "c++","python","dart"]
print(fruits,languages)
print(languages[2])
print(languages[-3])


#assigning multiple variables
a,b,c = 5, 7, "Hello world"
print(a) # prints 5
print(b) # prints 7
print(c) # prints Hello World


#python is case sensitive
num = 55
Num = 510
print(num) #5
print(Num) #510

#Tuple Data Type- same as list but once created cannot be changed
products = ('XBox', 499.99, "Habibi", 23)
print(products)

#Set Data Type-unordered collection of unique items

student_ids = {112, 114, 117, 113}
print(student_ids)

#Dictionary Data Type-orderd collection of items
capital_city = {"Kenya": "Nairobi", "Nigeria": "Lagos"}
